def init_shutong_dir(fb: FileBridge) -> list[str]:
    """初始化 .shutong/ 目录，返回创建的文件列表（已存在的不覆盖）"""
    created: list[str] = []

    for filename, content in TEMPLATES.items():
        path = f".shutong/{filename}"
        if not fb.file_exists(path):
            fb.write_file(path, content)
            created.append(path)

    for subdir in ["specs", "handoffs"]:
        gitkeep = f".shutong/{subdir}/.gitkeep"
        if not fb.file_exists(gitkeep):
            fb.write_file(gitkeep, "")
            created.append(gitkeep)

    return created
